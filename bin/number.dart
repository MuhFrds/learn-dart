void main() {
  // tipe data number di dart ada 2 int dan double
  // int untuk bilangan bulat dan double untuk bilangan desimal

  int number1 = 10;
  double number2 = 10.5;

  print(number1);
  print(number2);

  // jika ingin membuat tipe data number yang bisa int ataupun double bisa menggunakan num

  num number = 10;
  print(number);

  number = 10.5;
  print(number);
}
